        int scltRecord = workRequestRMJTable.getSelectedRow();
        
        if (scltRecord < 0){
            JOptionPane.showMessageDialog(null,"Kindly select a row before proceeding");
        }
        
        YieldRequest request = (YieldRequest)workRequestRMJTable.getValueAt(scltRecord, 0);
        request.setStatus("Forwarded to Producer ");
        populateSuppRequestTable();
        userAccount.getWorkQueue().getWorkRequestList().add(request);
        
        Organization org = null;
        for (Organization org23 : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (org23 instanceof OrgProduction){
                org = org23;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
        }
        
